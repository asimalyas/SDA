package model;

public abstract class TransportOption {
    private String name;
    private double cost;

    public TransportOption(String name, double cost) {
        this.name = name;
        this.cost = cost;
    }

    public String getName() {
        return name;
    }

    public double getCost() {
        return cost;
    }

    public abstract void displayDetails();
}

